package com.example;

public class ComputeValues {
    public static void main(String[] args){
        // Initializing variables x and y with values respectively and print the result
        int x = 0;
        int y = 1;

        System.out.println(x);
        System.out.println(y);

        //Performing some arithmetic and print the new values of x and y respectively
        x += 3;
        y += x;

        System.out.println(x);
        System.out.println(y);
    }

}
